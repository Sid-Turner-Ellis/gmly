import { Transaction, ethers } from "ethers";

let ethersProvider = null;

const wait = (seconds: number) =>
  new Promise((resolve) => setTimeout(resolve, seconds * 1000));

export const getEthersProvider = async () => {
  if (!ethersProvider) {
    ethersProvider = new ethers.providers.JsonRpcProvider(
      process.env.JSON_RPC_URL,
    );

    await Promise.race([
      ethersProvider.ready,
      new Promise(async (_, reject) => {
        await wait(7);
        console.warn("Ethers provider is taking longer than expected...");
        await wait(8);
        reject(
          `Couldn't connect to ethers provider on url: ${ethersProvider.connection.url}`,
        );
      }),
    ]);
  }

  return ethersProvider;
};

export const getGamerlyContract = async () => {
  const provider = await getEthersProvider();
  const signer = new ethers.Wallet(
    process.env.GAMERLY_SMART_CONTRACT_OWNER_PRIVATE_KEY,
    provider,
  );

  const gamerlyContract = new ethers.Contract(
    process.env.GAMERLY_SMART_CONTRACT_ADDRESS,
    gamerlyAbi,
  );

  return gamerlyContract.connect(signer);
};

